#  Comperation operators compare values and returns true or false
print(5 > 3)  # Grater than
print(7 < 2)  # Less than
print(5 >= 5)  # Grater than or equal to
print(4 <= 4)  # Less than or equal to
print(7 == 7)  # equal to (double equals, not single)
print(5 != 3)  #  Not equal to

#  you can compare vaibales as well:
a = 10
b = 7
print(a > b)
print(a == b)